DROP TABLE IF EXISTS NEWS;

CREATE TABLE NEWS
(
    ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
    joining_date DATETIME NOT NULL,
    category VARCHAR_IGNORECASE(30) NOT NULL,
    head VARCHAR_IGNORECASE(100) NOT NULL,
    content VARCHAR_IGNORECASE(10000) NOT NULL,
     PRIMARY KEY (ID)
) ;



